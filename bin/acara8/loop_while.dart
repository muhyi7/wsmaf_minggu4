void main() {
  print('LOOPING PERTAMA');

  int i = 2; // Inisialisasi nilai i
  while (i <= 20) {
    print('$i - I love coding');
    i += 2; // Increment i dengan 2
  }

  print('LOOPING KEDUA');

  int j = 20; // Inisialisasi nilai j
  while (j >= 2) {
    print('$j - I will become a mobile developer');
    j -= 2; // Decrement j dengan 2
  }
}
